public class Escape {
    public static void main(String[] args) {
        // escape sequence
        System.out.println("hello\"abhi\"dada");
        System.out.println("hello \\abhi\\ dada"); // for backslash
        System.out.println("hello! \ni am abhi"); // to write in other line
        System.out.println("hello abhis\b dada"); // to escape the last word of the string


    }
}
