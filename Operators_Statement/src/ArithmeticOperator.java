public class ArithmeticOperator {
    public static void main(String[] args) {
        int a= 8;
        int b= 5;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);

        double x= 2.5;
        double y=3.5;
        System.out.println(x+y);
        System.out.println(x/y);
        System.out.println(x-y);
        System.out.println(x*y);

        // modulus operator
        System.out.println(a%b);


    }
}
